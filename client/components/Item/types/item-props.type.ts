export type IItemProps = {
  /**
   * Whether the item is disabled
   */
  disabled?: boolean

  /**
   * When true, the item will not have the hover effect
   */
  noHoverEffect?: boolean

  /**
   * Whether the item is readonly
   */
  readonly?: boolean

  /**
   * The tag (HTML tag) of the item
   */
  tag?: string
}
